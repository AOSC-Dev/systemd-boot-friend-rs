    local i cur prev opts cmd
    COMPREPLY=()
    cur="${COMP_WORDS[COMP_CWORD]}"
    prev="${COMP_WORDS[COMP_CWORD-1]}"
    cmd=""
    opts=""

    for i in ${COMP_WORDS[@]}
    do
        case "${cmd},${i}" in
            ",$1")
                cmd="sbf"
                ;;
            sbf,config)
                cmd="sbf__config"
                ;;
            sbf,help)
                cmd="sbf__help"
                ;;
            sbf,init)
                cmd="sbf__init"
                ;;
            sbf,install-kernel)
                cmd="sbf__install__kernel"
                ;;
            sbf,list-available)
                cmd="sbf__list__available"
                ;;
            sbf,list-installed)
                cmd="sbf__list__installed"
                ;;
            sbf,remove-kernel)
                cmd="sbf__remove__kernel"
                ;;
            sbf,set-default)
                cmd="sbf__set__default"
                ;;
            sbf,set-timeout)
                cmd="sbf__set__timeout"
                ;;
            sbf,update)
                cmd="sbf__update"
                ;;
            sbf__help,config)
                cmd="sbf__help__config"
                ;;
            sbf__help,help)
                cmd="sbf__help__help"
                ;;
            sbf__help,init)
                cmd="sbf__help__init"
                ;;
            sbf__help,install-kernel)
                cmd="sbf__help__install__kernel"
                ;;
            sbf__help,list-available)
                cmd="sbf__help__list__available"
                ;;
            sbf__help,list-installed)
                cmd="sbf__help__list__installed"
                ;;
            sbf__help,remove-kernel)
                cmd="sbf__help__remove__kernel"
                ;;
            sbf__help,set-default)
                cmd="sbf__help__set__default"
                ;;
            sbf__help,set-timeout)
                cmd="sbf__help__set__timeout"
                ;;
            sbf__help,update)
                cmd="sbf__help__update"
                ;;
            *)
                ;;
        esac
    done

    case "${cmd}" in
        sbf)
            opts="-h -V --help --version init update install-kernel remove-kernel list-available list-installed config set-default set-timeout help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 1 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        sbf__config)
            opts="-h --help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 2 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        sbf__help)
            opts="init update install-kernel remove-kernel list-available list-installed config set-default set-timeout help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 2 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        sbf__help__config)
            opts=""
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 3 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        sbf__help__help)
            opts=""
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 3 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        sbf__help__init)
            opts=""
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 3 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        sbf__help__install__kernel)
            opts=""
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 3 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        sbf__help__list__available)
            opts=""
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 3 ]] ; then
